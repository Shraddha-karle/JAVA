package com.company;

public class data_types {
    public static void main(String[] args) {
        int a=23;
        char b='z';
        float c=2.3f;
        double d=2.00;
        short f=0;
        boolean e= true;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(f);
        System.out.println(e);
    }
}
